#include "3-calc.h"
/**
 * op_add - adds 2 num
 * @a: int a
 * @b: int b
 * Return: sum of numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub b from a
 * @a: int a
 * @b: int b
 * Return: result of sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 nums
 * @a: int a
 * @b: int b
 * Return: multiplication of numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a by b
 * @a: int a
 * @b: int b
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets modulus of b in a
 * @a: int a
 * @b: int b
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}
